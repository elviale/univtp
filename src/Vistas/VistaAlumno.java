    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        String nombre;
        int dni;
        String domicilio;
        LocalDate fechaNac;
        Alumno alumno;
        
        nombre =jtNombre.getText();
        dni = Integer.parseInt(jtDni.getText());
        domicilio=jtDomicilio.getText();
        fechaNac =LocalDate.parse(jtFechaNac.getText(),DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        alumno = new Alumno(nombre,dni,domicilio,fechaNac);
        ad.guardarAlumno(alumno);
        jtID.setText(alumno.getId_alum()+"");
    }//GEN-LAST:event_btGuardarActionPerformed

    private void btActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btActualizarActionPerformed
        String nombre;
        int dni;
        String domicilio;
        LocalDate fechaNac;
        Alumno alumno;
        if (jtID.setText()!= null){
            nombre =jtNombre.getText();
            dni = Integer.parseInt(jtDni.getText());
            domicilio=jtDomicilio.getText();
            fechaNac =LocalDate.parse(jtFechaNac.getText(),DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            alumno = new Alumno(nombre,dni,domicilio,fechaNac);
            ad.actualizarAlumno(alumno); 
            } 
    }//GEN-LAST:event_btActualizarActionPerformed

